/**
 * add - adds two integers and returns the result
 * @first_number: to be added to second_number
 * @second_number: to be added to first_number
 * Return: sum of first_number and second_number
 */
int add(int first_number, int second_number)
{
	int result;

	result = first_number + second_number;

	return (result);
}
